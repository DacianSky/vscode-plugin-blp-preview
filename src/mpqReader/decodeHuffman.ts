class TreeItem {
    prev: TreeItem | null = null;
    next: TreeItem | null = null;
    value: number = 0;
    weight: number = 0;
    parent: TreeItem | null = null;
    childLo: TreeItem | null = null;
    childHi: TreeItem | null = null;

    remove() {
        if (this.next !== null) {
            if (this.prev) {
                this.prev.next = this.next;
            }
            if (this.next) {
                this.next.prev = this.prev;
            }
            this.next = this.prev = null;
        }
    }

    insertAfter(where: TreeItem) {
        this.prev = where;
        this.next = where.next;
        if (where.next) {
            where.next.prev = this;
        }
        where.next = this;
    }

    insertBefore(where: TreeItem) {
        this.next = where;
        this.prev = where.prev;
        if (where.prev) {
            where.prev.next = this;
        }
        where.prev = this;
    }
}


class BitsReader {
    total: number = 0;
    byteIndex = 0;
    bitBuffer = 0;
    bitNumber = 0;

    constructor(public data: Uint8Array) {
        this.total = data.byteLength * 8;
    }

    unsignedRightShift(bitBuffer: number, bits: number) {
        if (bits >= 32 || bits < -32) {
            const m = (bits / 32);
            bits = bits - (m * 32);
        }
        if (bits < 0) {
            bits = 32 + bits;
        }
        if (bits === 0) {
            return ((bitBuffer >> 1) & 0x7fffffff) * 2 + ((bitBuffer >> bits) & 1);
        }
        if (bitBuffer < 0) {
            bitBuffer = (bitBuffer >> 1);
            bitBuffer &= 0x7fffffff;
            bitBuffer |= 0x40000000;
            bitBuffer = (bitBuffer >> (bits - 1));
        } else {
            bitBuffer = (bitBuffer >> bits);
        }
        return bitBuffer;
    }

    getBit(bits: number) {
        while (this.bitNumber < bits && this.byteIndex < this.data.length) {
            const value = this.data[this.byteIndex++];

            this.bitBuffer |= (value & 0xFF) << this.bitNumber;

            this.bitNumber += 8;
        }

        const result = this.bitBuffer & ((1 << bits) - 1);

        this.bitBuffer = this.unsignedRightShift(this.bitBuffer, bits);
        this.bitNumber -= bits;

        return result;
    }

    canRead() {
        return this.byteIndex < this.data.byteLength;
    }
}

class THuffmannTree {
    // Head of the linear item list
    pFirst: TreeItem;                        // Pointer to the highest weight item
    pLast: TreeItem;                         // Pointer to the lowest weight item
    ItemsByByte: TreeItem[] = [];
    MinValidValue = 1;
    bIsCmp0 = 0;

    constructor() {
        // 构建一个链表 首尾相连
        this.pFirst = new TreeItem();
        this.pLast = new TreeItem();
        this.pFirst.next = this.pLast;
        this.pFirst.prev = this.pLast;
        this.pLast.prev = this.pFirst;
        this.pLast.next = this.pFirst;
    }

    createNewItem(value: number, weight: number) {
        const treeItem = new TreeItem();

        // Fill the rest of the item
        treeItem.value = value;
        treeItem.weight = weight;
        treeItem.parent = null;
        treeItem.childLo = null;
        treeItem.childHi = null;
        return treeItem;
    }


    findHigherOrEqualItem(node: TreeItem, weight: number) {
        while (node !== this.pFirst) {
            if (node.weight >= weight) {
                return node;
            }
            node = node.prev!;
        }

        return this.pFirst;
    }

    fixupItemPosByWeight(node: TreeItem, maxWeight: number) {
        if (node.weight < maxWeight) {
            // Find an item that has higher weight than this one
            const higherItem = this.findHigherOrEqualItem(this.pLast, node.weight);
            node.remove();
            node.insertAfter(higherItem);
        } else {
            maxWeight = node.weight;
        }
        return maxWeight;
    }

    buildTree(compressionType: number) {
        if (compressionType > 0x08) {
            throw new Error(`huffman unsupport compress type ${compressionType}`);
        }
        const weightTable = WeightTables[compressionType & 0x0F];
        let maxWeight = 0;
        // 构建一个按照weight 排序的链表
        for (let i = 0; i < 0x100; i++) {
            const weight = (weightTable[i] & 0xFF);
            if (weight === 0) {
                continue;
            };

            const node = this.createNewItem(i, weight);
            node.insertAfter(this.pFirst);
            maxWeight = this.fixupItemPosByWeight(node, maxWeight);
            this.ItemsByByte[i] = node;
        }

        const end1 = this.createNewItem(0x100, 1);
        end1.insertBefore(this.pLast);
        this.ItemsByByte[0x100] = end1;
        const end2 = this.createNewItem(0x101, 1);
        end2.insertBefore(this.pLast);
        this.ItemsByByte[0x101] = end2;

        // 构建树
        let pChildLo = this.pLast.prev;
        let pChildHi: TreeItem;
        while (pChildLo !== this.pFirst) {
            // Also get and verify the higher-weight child
            pChildHi = pChildLo!.prev;
            if (pChildHi === this.pFirst) {
                break;
            }

            const newItem = this.createNewItem(0, pChildHi.weight + pChildLo?.weight);
            newItem.insertAfter(this.pFirst);

            // Link both child items to their new parent
            pChildLo!.parent = newItem;
            pChildHi.parent = newItem;
            newItem.childLo = pChildLo;
            newItem.childHi = pChildHi;

            // Fixup the item's position by its weight
            maxWeight = this.fixupItemPosByWeight(newItem, maxWeight);

            // Get the previous lower-weight child
            pChildLo = pChildHi.prev;
        }
    }

    insertNewBranchAndRebalance(lastValue: number, value: number) {
        const lastNode = this.pLast.prev;
        const hiChild = this.createNewItem(lastValue, lastNode!.weight);
        hiChild.insertBefore(this.pLast);
        hiChild.parent = lastNode;
        lastNode!.childHi = hiChild;
        this.ItemsByByte[lastValue] = hiChild;

        const loChild = this.createNewItem(value, 0);
        loChild.insertBefore(this.pLast);
        loChild.parent = lastNode;
        lastNode!.childLo = loChild;
        this.ItemsByByte[value] = loChild;

        this.incWeightsAndRebalance(loChild);
    }

    // Climb up the tree and increment weight of each tree item
    incWeightsAndRebalance(pItem: TreeItem | null) {
        for (; pItem !== null; pItem = pItem.parent) {
            // Increment the item's weight
            pItem.weight++;

            // Find a previous item with equal or greater weight, which is not equal to this item
            const pHigherItem = this.findHigherOrEqualItem(pItem.prev!, pItem.weight);
            const pChildHi = pHigherItem.next;

            // If the item is not equal to the tree item, we need to rebalance the tree
            if (pChildHi !== pItem) {
                // Move the previous item to the RIGHT from the given item
                pChildHi!.remove();
                pChildHi!.insertAfter(pItem);

                // Move the given item AFTER the greater-weight tree item
                pItem.remove();
                pItem.insertAfter(pHigherItem);

                // We need to maintain the tree so that pChildHi->Weight is >= pChildLo->Weight.
                // Rebalance the tree accordingly.
                const pChildLo = pChildHi!.parent!.childLo!;
                let pParent = pItem.parent!;
                if (pParent.childLo === pItem) {
                    pParent.childLo = pChildHi;
                }
                if (pChildLo === pChildHi) {
                    pChildHi.parent!.childLo = pItem;
                }
                pParent = pItem.parent!;
                pItem.parent = pChildHi!.parent;
                pChildHi!.parent = pParent;
            }
        }
    }

    decodeOneByte(is: BitsReader) {
        let node = this.pFirst.next;
        // Step down the tree until we find a terminal item
        while (node!.childLo !== null) {
            // If the next bit in the compressed stream is set, we get the higher-weight
            // child. Otherwise, get the lower-weight child.
            node = is.getBit(1) ? node!.childHi : node!.childLo;
        }
        return node!.value;
    }

    decompress(bytes: Uint8Array) {
        const stream = new BitsReader(bytes);
        const out: number[] = [];

        const compressionType = stream.getBit(8);
        this.buildTree(compressionType);

        const bIsCmp0 = (compressionType === 0) ? 1 : 0;

        let decompressedValue: number;

        while ((decompressedValue = this.decodeOneByte(stream)) !== 0x100) {
            // Huffman tree needs to be modified
            if (decompressedValue === 0x101) {
                // The decompressed byte is stored in the next 8 bits
                decompressedValue = stream.getBit(8);

                this.insertNewBranchAndRebalance(this.pLast.prev!.value, decompressedValue);

                if (bIsCmp0 === 0) {
                    this.incWeightsAndRebalance(this.ItemsByByte[decompressedValue]);
                }
            }

            // A byte successfully decoded - store it in the output stream
            // * pbOutBuffer++ = (unsigned char) DecompressedValue;
            out.push(decompressedValue);
            if(!stream.canRead()) {
                break;
            }
            // if (pbOutBuffer >= pbOutBufferEnd)
            //     break;

            if (bIsCmp0) {
                this.incWeightsAndRebalance(this.ItemsByByte[decompressedValue]);
            }
        }

        return Uint8Array.from(out);
    }
}

export default function decodeHuffman(bytes: Uint8Array): Uint8Array {
    const node = new THuffmannTree();
    return node.decompress(bytes);
}
